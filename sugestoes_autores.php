   <h3>Sugest&otilde;es aos Autores</h3>

   <p>
   A seguir são apresentadas sugest&otilde;es endereçadas aos autores que desejam submeter seus
   artigos para publica&ccedil;&atilde;o e apresenta&ccedil;&atilde;o na ERRC/WRSeg 2019. Cada sugestão é acompanhada de referências que podem ajudar na sua concretização.
   </p>
   <p>
  
   
   A primeira sugestão é <a href="https://images.app.goo.gl/LTKeqk9XYN4YXEDx7"><b>planejar</b></a> o que será escrito antes de começar a escrever. 
   </p>
   <ul class="lista">
      <li class="text-justify">Dicas gerais são apresentadas em <a href="http://blizzard.cs.uwaterloo.ca/keshav/home/Papers/data/10/ogres2.pdf">&ldquo;Ogres, Villagers, & the Art of Writing&rdquo;</a>, escrito por S. Keshav.
      </li>

      <li class="text-justify">
      Para planejar aspectos mais específicos do artigo a ser submetido para a ERRC, pode ser útil analisar o formulário que será empregado pelos revisores.
      Use o formulário como guia para auto-avaliar o seu artigo. Verifique, por exemplo, no quesito originalidade, se o seu
      artigo compara o trabalho com outros na literatura; no quesito m&eacute;rito t&eacute;cnico, se o artigo
      diz claramente ao revisor qual a contribui&ccedil;&atilde;o do trabalho.
      </li>

      <li class="text-justify">
      Na prepara&ccedil;&atilde;o do seu artigo, use o formato exigido. O ERRC/WRSeg 2019 exige o <a href="http://www.sbc.org.br/documentos-da-sbc/category/169-templates-para-artigos-e-capitulos-de-livros">modelo da SBC</a>. Atente para o limite de espaço.
      </li>   
   </ul> 
   

<br>
    
   <p>
   A segunda sugestão é <b>estudar</b> o processo de escrita. 
   </p>
   <ul class="lista">
      <li class="text-justify">
      Um dos melhores recursos dispon&iacute;veis em portugu&ecirc;s &eacute; o <a href="http://www.inf.ufpr.br/pos/techreport/RT_DINF004_2004.pdf">&ldquo;Pequeno Manual da Escrita T&eacute;cnica&rdquo;</a>, escrito
      por Roberto Hexsel. Esse material &eacute; uma boa leitura para alunos ao escreverem seus
      trabalhos. Um dos lemas do texto &eacute; &ldquo;leia o que escreveu&rdquo;.
      </li>
      
      <li class="text-justify">
      Outro bom material em português é o <a href="http://1.49.d5.sl.pt/">&ldquo;Como escrever um Artigo Cient&iacute;fico ou T&eacute;cnico: Alguns Aspectos&rdquo;</a>.
      </li>
      
      <li class="text-justify">
      &Eacute; importante tamb&eacute;m entender os principais aspectos elencados em <a href="https://www.scribd.com/document/11655966/Como-Nao-Escrever-um-Artigo-Cientifico">&ldquo;Como (n&atilde;o) Escrever um Artigo T&eacute;cnico-Cient&iacute;fico&rdquo;</a>.
      </li>

      <li class="text-justify">
      <a href="http://www.ic.unicamp.br/~rodolfo/dicas/escrita.html">&ldquo;Dicas sobre escrita&rdquo;</a> e <a href="http://www.ic.unicamp.br/~rodolfo/dicas/apresentacoes.html">&ldquo;Dicas sobre apresenta&ccedil;&otilde;es&rdquo;</a>, elaboras por Rodolfo de Azevedo, cont&eacute;m  dicas importantes e &uacute;teis para escrita e apresentação de artigos.
      </li>

      <li class="text-justify">
      <a href="http://www.cs.columbia.edu/~hgs/etc/writing-style.html">&ldquo;Writing Technical Articles&rdquo;</a>,
      escrito por Henning Schulzrinne, descreve em detalhes o processo de se elaborar um artigo t&eacute;cnico de qualidade na &aacute;rea da computa&ccedil;&atilde;o, tratando tamb&eacute;m aspectos de estilo.
      </li> 

      <li class="text-justify">
      <a href="https://www.bartleby.com/141/index.html">&ldquo;The Elements of Style&rdquo;</a> &eacute; uma refer&ecirc;ncia interessante para compreender e aplicar elementos de estilo na escrita.
      </li> 

      <li class="text-justify">
      A Introdu&ccedil;&atilde;o (<a href="http://www.cs.columbia.edu/~hgs/etc/intro-style.html">&ldquo;Writing a good introduction&rdquo;</a>) é fundamental para &ldquo;converter&rdquo; um potencial leitor, que se interessou pela introdução, em um leitor do trabalho completo.
      </li>
      
