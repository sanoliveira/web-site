      <li class="text-justify">
      &ldquo;On Writing Well&rdquo;, escrito por William Zinsser, &eacute; um dos melhores e mais sucintos livros sobre como escrever bem.  Apesar de ser um livro voltado para escrita em ingl&ecirc;s, boa parte de suas dicas podem ser aplicadas em outras l&iacute;nguas, como portugu&ecirc;s. Uma  dica apresentada é dar preferência para palavras simples, frases curtas e objetivas.
      </li>

      <li class="text-justify">
      O post <a href="https://goo.gl/s9AeLs">&ldquo;The Six Best Tips From On Writing Well&rdquo;</a> sumariza as principais
      dicas do livro "On Writing Well".
      </li>
      
      <li class="text-justify">
      <a href="https://sivers.org/book/OnWritingWell">&ldquo;As notas de Derek Sivers&rdquo;</a>, do livro &ldquo;On Writing Well&rdquo;,
      cont&eacute;m v&aacute;rias dicas para melhorar substancialmente a habilidade de escrita de qualquer pessoa.
      Tente seguir as dicas para obter resultados cada vez melhores.
      </li>
   </ul>

<br>
   <p>
   A terceira sugestão é <b>revisar</b> o artigo. 
   </p>
   <ul class="lista">
      <li class="text-justify">
      Fa&ccedil;a uma revis&atilde;o adequada do conteúdo do artigo antes de submeter. Pe&ccedil;a para seus 
      co-autores mais experientes em escrita revisar cuidadosamente a vers&atilde;o final. Alternativamente, 
      pe&ccedil;a para amigos ou profissionais em revis&atilde;o de texto dar um toque de qualidade ao seu texto. 
      </li>

      <li class="text-justify">
      Trocar a cor da fonte ou o meio (eletrônico/impresso) pode ajudar a recobrar a concentração em trechos que já tenham sido revisados múltiplas vezes anteriormente.
      </li>

      <li class="text-justify">
      Faça um checklist sobre a formatação do artigo. Use um corretor ortográfico para evitar erros básicos, como <a href="https://ciberduvidas.iscte-iul.pt/artigos/rubricas/idioma/a-virgula-em-4-regras-simples/3191">uso da vírgula</a>, crase e concord&acirc;ncia nominal. Em seguida, verifique se todos elementos auxiliares, como figuras, tabelas, equações, são referenciados no texto e de maneira adequada. Para cada citação, verifique: se (i) ela é pertinente para trabalho, (ii) se ela é coerente com o artigo referenciado e não é uma referência indireta, e (iii) aponta para o artigo correto. Verifique se todas as referências seguem mesmo padrão, por exemplo, quanto ao grau de detalhes. 
      </li>
      
      <li class="text-justify">
      Por fim, imprima duas vias do artigo, uma colorida e outra preto e branco, e procure detectar possíveis problemas de impressão.
      </li>
   </ul>
