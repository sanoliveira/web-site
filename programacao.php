    <h3>Análise de tráfego de máquinas virtuais na rede de controle de nuvens computacionais baseadas em OpenStack</h3>
    <p><b>Data:</b> a ser definida </p>
    <p><b>Autores:</b> Adnei Willian Donatti, Charles Christian Miers, Guilherme Piêgas Koslovski, Maurício Aronne Pillon</p>
    
    <p>
    <b>Resumo:</b>A computação em nuvem é um paradigma para disponibilização de infraestrutura computacional. Este paradigma muda a localização da infraestrutura para a rede, no sentido de possibilitar a distribuição ou realocação de carga entre os diversos recursos computacionais conectados a rede. Portanto, emprega-se virtualização para reduzir os custos relacionados com o gerencia- mento e manutenção de recursos de hardware e software [Vaquero et al. 2008, Mell and Grance 2011]. Segundo [Jadeja and Modi 2012], o usuário pode ter acesso a uma infraestrutura virtualizada externa mediante pagamento pelo uso, ou com a implantação de uma nuvem privada, cuja utilização alinha-se às necessidades de uma organização. Segundo [Wang and Ng 2010], questões relacionadas ao desempenho de uma nuvem dependem da identificação de comportamentos e operações que ocorrem durante o seu uso. Além disso, percebe-se, que há uma necessidade de identificar operações, muitas das quais são operações que ocorrem no nível da camada de rede e demandam análise e compreensão do que está trafegando e sua finalidade. Neste sentido, a caracterização de tráfego auxilia este entendimento, por meio do emprego de técnicas e métodos que possibilitam a coleta e identificação de forma sistematizada. Apesar de não ter sido identificada uma definição formal ou me ́todo para caracterização de tráfego, este termo refere-se à minuciosa análise do tráfego de uma rede, buscando entender seu comportamento e também as consequências que derivam deste comportamento. 
    </p> 
    <br><br>
    
    
        
    <h3>Web Application Firewalls (WAFs): Teoria e Prática</h3>
    <p><b>Data:</b> a ser definida </p>
    <p><b>Autores:</b> Felipe Melchior, Diego Kreutz, Mauricio Fiorenza, Fernando Flora, Douglas Macedo</p>
    
    <p>
    <b>Resumo:</b> Relatórios especializados indicam que aproximadamente 90% das aplicações disponibilizadas na Web apresentam algum tipo de vulnerabilidade. A boa notícia é que estudos recentes apontam que frameworks de desenvolvimento de software e ferramentas de segurança como Web Application Firewalls conseguem mitigar a exploração de mais de 70% das vulnerabilidades de uma aplicação Web. Neste contexto, o objetivo deste minicurso é demonstrar, na teoria e na prática, como a utilização, a escolha e a otimização de WAFs pode contribuir de forma significativa na proteção de aplicações Web contra os principais tipos de ataques que buscam explorar as vulnerabilidades mais recorrentes em aplicações Web. Durante o minicurso, iremos apresentar conceitos teóricos fundamentais para a adequada compreensão, configuração e utilização de Web Application Firewalls. Além disso, iremos utilizar máquinas virtuais pré-configuradas para demonstrar na prática o funcionamento e impacto de WAFs na proteção de aplicações Web. Como resultado, esperamos contribuir de forma significativa na conscientização de estudantes e profissionais de computação sobre a importância do uso de WAFs na proteção de aplicações Web.
    </p> 
